                            <h2>Climate in the Maldives</h2>

                            <p>Balmy, sunny, azure skies, and crystal clear blue water most of the year, but the
                                Maldives has a tropical monsoon climate, which is affected by the large landmass of
                                South Asia to the north. Two seasons dominate Maldives' weather: the dry season
                                associated with the winter northeastern monsoon and the rainy season or southeast
                                monsoon. The best weather and diving conditions are generally from late October to mid
                                May during the northeastern monsoon. The wettest time of the year is June through
                                August. with annual rainfall averages 254 centimetres (100 in) in the north and 381
                                centimetres (150 in) in the south.</p>

                            <h2>Entering the Maldives</h2>

                            <p>Visas – In most cases it is not necessary to arrange a visa before arriving to the
                                Republic of Maldives. A 30 day entry permit will be granted to visitors on arrival at
                                the Male International Airport. The permit will be granted based on a valid return
                                airline ticket, and valid passport. Entry regulations do vary by country so it is best
                                to check with your embassy about requirements, prior to arrival.</p>

                            <P>Passport - Your passport must be valid for at least 6 months beyond your period of stay.
                                US citizens require a passport that must be valid for 6 months from date of entry. It is
                                the travelers’ responsibility to ensure they have the proper documentation to
                                travel.</P>

                            <P>Vaccinations – There are no compulsory vaccinations are required to enter the Maldives,
                                but travelers should be in good health when departing their home country. Vaccination
                                requirements are subject to change and should be confirmed before arrival. </P>

                            <h2>Arrival and Boarding Your Liveaboard</h2>

                            <p>To avoid issues from flight delays most divers will fly into the Male International
                                Airport (MLE) the day before liveaboard departure and have an overnight stay on Male or
                                at the Hulhule Male Airport Hotel. The airport is a ten-minute boat ride from the
                                capital city of Male. </p>

                            <p>It possible to arrive the day of liveaboard departure but your flight should be scheduled
                                to arrive prior to 12:00 pm. Arriving a day early or early morning flights on the day of
                                departure are highly recommend. The main meeting point for ALL liveaboard departures is
                                always Male International airport. You will be given a meeting time and location within
                                the airport to meet your transport agent. The agent holding a sign with your designated
                                liveaboard name waiting for you at the pre established time. If your arriving on the day
                                of departure you may be met upon arrival at the airport depending on the time or your
                                arrival. Late arrivals can be transported to the liveaboard, for a nominal charge,
                                provided the boat has not left the harbor.</p>

                            <h2>Communicating with Home</h2>

                            <p>Cell phone coverage is wide spread throughout the Maldives and a signal is even available
                                at many points throughout our liveaboard trips. A local SIM card can be purchased at the
                                airport upon arrival or you can use the roaming services of your home country.</p>

                            <p>Internet and email will be easily accessible throughout the archipelago where an internet
                                signal is available. Most of our Maldives liveaboards are equipped with wi-fi either
                                free of charge or at a nominal rate. A prepaid internet stick can also be purchased upon
                                arrival at Male International airport.</p>

                            <h2>Local Language</h2>

                            <p>The local language or native tongue is Dhivehi; however, English is widely understood and
                                spoken. Liveaboard personnel and most others in the tourist industry will be English
                                speaking.</p>

                            <h2>Currency and Payment</h2>

                            <p>Visa and MasterCard debit and credit cards are widely accepted throughout the Maldives as
                                well as US dollars. The local currency of the Maldives is the Rufiyaa and is also an
                                acceptable form of payment. Most of our liveaboards will accept credit cards and US
                                dollars while onboard. ATM machines will be available in the capital city of Male, but
                                it is best to arrive with a back up of clean US $100 bills in addition to your credit
                                cards. No personal checks will be accepted in the Maldives.</p>

                            <h2>Electricity and Power</h2>

                            <p>Resorts and liveaboard boats in the Maldives and use 220/240 volt AC at 50 cycles. There
                                is no standard socket type so you must bring an international adapter for your own use.
                                Plugs are either the 3 square pin - 13 amp British type or they are the 2-pin European
                                style. If you are used to a 110V supply you will need a small transformer to convert
                                from 240V to 110V.</p>
