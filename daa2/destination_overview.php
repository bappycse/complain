                            <h2>The Maldives Best Diving</h2>

                            <p>At 96 km long and 33 km wide Ari Atoll is home to more than 70 awe inspiring islands and
                                a vast area of open ocean and lagoons. With astounding visibility of up to 50+ meters
                                and depths exceeding 2,000 meters divers can expect nothing less than staggering high
                                adrenaline filled diving with schooling pelagic's, massive Manta's, feeding Whale
                                Sharks, enticing wrecks, schooling eagle rays, and a variety of oceanic sharks.</p>

                            <p>The Maldives are not well known for reefs of magnificent color, or the destination of
                                macro bio-diversity, however, there are few places left on the planet that have the
                                ability to deliver the consistent big fish action that is common place here. Be one of
                                the few to experience this legendary and thrilling dive destination on a <a href="">Maldives
                                    Liveaboard.</a></p>

                            <h2>Maldives Diving Season</h2>

                            <p><span class="bold">When to dive the Maldives?</span> That depends on what you are looking
                                for. The Maldives have a tropical climate year round with warm temperatures and a great
                                deal of sunshine. Diving is a year round activity but different times of the year offer
                                different advantages.</p>
                            <ul class="unstyled ">
                                <li><i class="fa fa-circle" aria-hidden="true"></i>Diving Season: Year Round (The best
                                    weather and diving is from November o May)
                                </li>
                                <li><i class="fa fa-circle" aria-hidden="true"></i>November through April has the best
                                    overall weather and diving conditions with guaranteed trip departures
                                </li>
                                <li><i class="fa fa-circle" aria-hidden="true"></i>December through March has the best
                                    overall visibility
                                </li>
                                <li><i class="fa fa-circle" aria-hidden="true"></i>April, May, and October are the
                                    months with the greatest probability of Whale Shark and Manta sightings
                                </li>
                            </ul>
                            <p>Liveaboard trips run throughout the year and offer excellent diving every month. Plan
                                your trip based on your expectations and schedule!</p>

                            <h2>How to Dive the Maldives – Liveaboards</h2>

                            <p>Because many of the dive sites and atolls in the Maldives are remote and away from the
                                resorts the best way to access this world class destination is on a Maldives Liveaboard
                                safari. Liveaboard operators offering diving cruises throughout the archipelago abide by
                                strict safety standards, are budget to luxury class, and are well equipped for oceanic
                                voyages.</p>

                            <p>Cruises will vary in length from 7 to 11 days to a huge variety of islands and dive sites
                                where the big pelagic’s are always in attendance. Check out our individual liveaboard
                                options for more complete and detailed information on diving throughout the
                                Maldives.</p>

                            <p><span class="redbold">Please note that space on quality liveaboards is limited and booking well in advance is necessary to guarantee your requested dates!</span>
                            </p>

