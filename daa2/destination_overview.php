                            <h2>Getting to the Maldives</h2>

                            <p>Male International Airport (IATA: MLE, ICAO: VRMM) is the main international airport in
                                the Maldives. It is located on Hulhule Island in the North Male Atoll, near the capital
                                island of Male. There are many direct international flights into Male from Europe, Asia,
                                and the U.S., making the Maldives a very easy destination to travel to. </p>

                            <p>Most Maldives liveaboard trips will depart directly from Hulhule Harbor, where the
                                airport is located. However; for liveaboard trips to the northern or southern atolls an
                                additional domestic or sea plane flight may be required. Depending on the liveaboard
                                trip selected a one way domestic flight from Male to the departure point or a round trip
                                domestic flight from Male to the departure point may be required. <a href="">Contact
                                    us </a>for departure point details.</p>

                            <p>On the day of liveaboard departure there will be transfer service provided to and from
                                your liveaboard and the airport in the Maldives. After arriving you will be greeted at
                                the airport and transferred to the boat, its that easy! Most liveaboards will arrange a
                                transfer time that is shortly after lunch on the day of departure. It is best to arrange
                                a flight into the Maldives that arrives before lunch. There are day rooms that can be
                                booked at the Hulhule Airport Hotel if you arrive into the Maldives in the early morning
                                and need a place to relax before the trip.</p>

                            <p>If you overnight in Male the night prior to departure you will need to make your way back
                                to the airport at the designated time for your pick up and transfer. The pick up
                                location for all Maldives liveaboard departures is the airport.</p>

                            <p class="navy-blue">Please note that to enter the Maldives you must have a minimum of 6
                                months before expiration left on your passport and one full clean page for your visa on
                                arrival.</p>

